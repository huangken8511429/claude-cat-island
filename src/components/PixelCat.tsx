function zoneColor(zone: number, t: CatTheme): string | null {
  switch (zone) {
    case 0: return null;       // transparent
    case O: return t.outline;
    case B: return t.body;
    case V: return t.belly;
    case E: return t.earInner;
    case I: return t.iris;
    case W: return "#ffffff";   // always white highlight
    case N: return t.nose;
    case C: return t.closedEye;
    case K: return t.cheek;
    case P: return t.pupil;
    case M: return t.mouth;
    case S: return t.shadow;
    default: return t.body;
  }
}

export default function PixelCat({ state, size = 3, themeIndex }: PixelCatProps) {
  const canvasRef = useRef<HTMLCanvasElement>(null);

  const theme = useMemo(() => {
    const idx = themeIndex ?? Math.floor(Math.random() * THEMES.length);
    return THEMES[idx % THEMES.length];
  }, [themeIndex]);

  const prefersReducedMotion = useMemo(
    () => window.matchMedia("(prefers-reduced-motion: reduce)").matches,
    [],
  );

  const canvasW = 32 * size;
  const canvasH = 36 * size; // extra space for jump animation + zzz

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d")!;

    const { frames, speed } = ANIM[state] || ANIM.idle;
    let tick = 0;
    let rafId: number;

    const draw = () => {
      ctx.clearRect(0, 0, canvasW, canvasH);
      ctx.imageSmoothingEnabled = false;

      const frameIdx = prefersReducedMotion
        ? 0
        : Math.floor(tick / speed) % frames.length;
      const sprite = frames[frameIdx];

      // vertical offset for jump / breathing
      let offY = size * 2;
      if (state === "done" && !prefersReducedMotion) {
        // bouncy celebration hop — abs(sin) so the cat always pops upward
        offY -= Math.abs(Math.sin(tick * 0.15)) * size * 2;
      }
      if (state === "sleeping" && !prefersReducedMotion) {
        offY += Math.sin(tick * 0.04) * size * 0.5;
      }

      // draw sprite
      for (let y = 0; y < 32; y++) {
        for (let x = 0; x < 32; x++) {
          const c = zoneColor(sprite[y][x], theme);
          if (c) {
            ctx.fillStyle = c;
            ctx.fillRect(x * size, y * size + offY, size, size);
          }
        }
      }

      // sleeping: floating zzz
      if (state === "sleeping") {
        const p = prefersReducedMotion ? 0 : tick * 0.035;
        ctx.fillStyle = theme.shadow;
        const zz = [
          { sz: size * 2.4, x: 26, y: 4, d: 0 },
          { sz: size * 1.8, x: 28, y: 1, d: 1 },
          { sz: size * 1.2, x: 30, y: -1.5, d: 2 },
        ];
        zz.forEach(({ sz, x, y, d }) => {
          ctx.globalAlpha = 0.5 + Math.sin(p + d) * 0.4;
          ctx.font = `bold ${sz}px "JetBrains Mono", monospace`;
          ctx.fillText("z", x * size, (y + Math.sin(p + d) * 0.8) * size + offY);
        });
        ctx.globalAlpha = 1;
      }

      // done: floating pink hearts
      if (state === "done") {
        const spots = [
          [2, 0], [28, 2], [0, 14], [30, 12], [14, 0], [16, 30],
          [4, 8], [26, 6],
        ];
        spots.forEach(([sx, sy], i) => {
          const a = prefersReducedMotion
            ? 0.7
            : Math.max(0, Math.sin(tick * 0.12 + i * 1.2));
          if (a > 0.15) {
            ctx.globalAlpha = a * 0.9;
            ctx.fillStyle = "#ff6b9d";
            const px = sx * size, py = sy * size + offY;
            // 3x3 pixel heart: two top bumps, wide middle, bottom tip
            ctx.fillRect(px, py, size, size);
            ctx.fillRect(px + size * 2, py, size, size);
            ctx.fillRect(px, py + size, size * 3, size);
            ctx.fillRect(px + size, py + size * 2, size, size);
          }
        });
        ctx.globalAlpha = 1;
      }

      tick++;
      if (prefersReducedMotion) return; // only draw one frame
      rafId = requestAnimationFrame(draw);
    };

    draw();
    return () => cancelAnimationFrame(rafId);
  }, [state, theme, size, canvasW, canvasH, prefersReducedMotion]);

  return (
    <canvas
      ref={canvasRef}
      width={canvasW}
      height={canvasH}
      className="pixel-cat-canvas"
      style={{ imageRendering: "pixelated", width: canvasW, height: canvasH }}
    />
  );
}

